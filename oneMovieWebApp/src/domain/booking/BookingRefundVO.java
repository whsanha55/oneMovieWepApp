package domain.booking;

public class BookingRefundVO {
	int rufundNo;
	String ticketNo;
	String rufundDate;
	int refundPrice;
	

	public BookingRefundVO() {
	}

	public int getRufundNo() {
		return rufundNo;
	}

	public void setRufundNo(int rufundNo) {
		this.rufundNo = rufundNo;
	}

	public String getTicketNo() {
		return ticketNo;
	}

	public void setTicketNo(String ticketNo) {
		this.ticketNo = ticketNo;
	}

	public String getRufundDate() {
		return rufundDate;
	}

	public void setRufundDate(String rufundDate) {
		this.rufundDate = rufundDate;
	}

	public int getRefundPrice() {
		return refundPrice;
	}

	public void setRefundPrice(int refundPrice) {
		this.refundPrice = refundPrice;
	}

	

	
}
