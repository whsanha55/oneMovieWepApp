package domain.booking;

public class BookingSnVO {
	String movieSn;
	String theaterSn;
	String dateSn;
	String turnSn;
	String SeatSn;
	int priceSn;

	public BookingSnVO() {
	
	}

	public String getMovieSn() {
		return movieSn;
	}

	public void setMovieSn(String movieSn) {
		this.movieSn = movieSn;
	}

	public String getTheaterSn() {
		return theaterSn;
	}

	public void setTheaterSn(String theaterSn) {
		this.theaterSn = theaterSn;
	}

	public String getDateSn() {
		return dateSn;
	}

	public void setDateSn(String dateSn) {
		this.dateSn = dateSn;
	}

	public String getTurnSn() {
		return turnSn;
	}

	public void setTurnSn(String turnSn) {
		this.turnSn = turnSn;
	}

	public String getSeatSn() {
		return SeatSn;
	}

	public void setSeatSn(String seatSn) {
		SeatSn = seatSn;
	}

	public int getPriceSn() {
		return priceSn;
	}

	public void setPriceSn(int priceSn) {
		this.priceSn = priceSn;
	}

	

}
